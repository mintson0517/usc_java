public class File_002_calculation {
    public static void main(String args[]) {
        int a = 10;// int 整数
        int b = 100;

        int sum1 = a + b;// plus
        int sum2 = a - b;// minus
        int sum3 = a * b;// multiplication
        int sum4 = a / b;// division
        int sum5 = a % b;// over

        System.out.print(sum1);
        System.out.print(sum2);
        System.out.print(sum3);
        System.out.print(sum4);
        System.out.print(sum5);
    }
}
